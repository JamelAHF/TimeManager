  def get_working_time!(id), do: Repo.get!(WorkingTime, id)


  def get_working_time_by_params!(params) do
    query = from wt in WorkingTime, where: wt.start > ^params["start_date"], where: wt.end <= ^params["end_date"], where: wt.user == ^params["user"], select: %{id: wt.id,start: wt.start,end: wt.end, user: wt.user}
    Repo.all(query)
  end


  def get_working_time_by_user!(userid) do
    query = from c in WorkingTime , where: c.user == ^userid, select: %{id: c.id, start: c.start, end: c.end, user: c.user}
    Repo.all(query)
  end

  def get_working_time_by_team!(params) do
    IO.inspect(params)
    query = from wt in WorkingTime, join: u in User, on: wt.user == u.id, where: wt.start > ^params["start_date"], where: wt.end <= ^params["end_date"], where: u.team_id == ^params["team"], select: %{id: wt.id,start: wt.start,end: wt.end, user: u.username}
    Repo.all(query)
  end

  def get_working_time_by_userparams!(params) do
    IO.inspect(params)
    query = from wt in WorkingTime, where: wt.start >= ^params["start"], where: wt.end <= ^params["end"], where: wt.user == ^params["id_user"], select: %{id: wt.id, start: wt.start, end: wt.end , user: wt.user}
    Repo.all(query)
  end

